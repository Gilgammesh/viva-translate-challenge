/*******************************************************************************************************/
// Importamos las dependencias //
/*******************************************************************************************************/
import { Router } from 'express';
import * as auth from '../controllers/auth/auth.controller';
import * as usuario from '../controllers/auth/usuario.controller';

/*******************************************************************************************************/
// Instanciamos router //
/*******************************************************************************************************/
const router: Router = Router();

/*******************************************************************************************************/
// Definimos las rutas //
/*******************************************************************************************************/
// Autenticaciones
router.post('/check', auth.check);
router.post('/login', auth.login);

// Usuarios
router.post('/usuarios', usuario.create);

/*******************************************************************************************************/
// Exportamos las rutas definidas en router por defecto //
/*******************************************************************************************************/
export default router;
