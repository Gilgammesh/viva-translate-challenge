/*******************************************************************************************************/
// Importamos las dependencias //
/*******************************************************************************************************/
import types from 'configs/types';

/*******************************************************************************************************/
// Interface de Action //
/*******************************************************************************************************/
interface IAction {
	type: string;
	payload: any;
}

/*******************************************************************************************************/
// Interface del Reducer //
/*******************************************************************************************************/
export interface ICuestionarioReducer {
	_id: string;
	nombre: string;
	descripcion?: string;
}

/*******************************************************************************************************/
// Estado inicial del Reducer //
/*******************************************************************************************************/
const initialState: ICuestionarioReducer | null = null;

/*******************************************************************************************************/
// Definimos el reducer y sus mÃ©todos //
/*******************************************************************************************************/
const cuestionarioReducer = (state = initialState, { type, payload }: IAction) => {
	switch (type) {
		case types.setCuestionario:
			return payload;
		case types.cleanCuestionario:
			return initialState;
		default:
			return state;
	}
};

/*******************************************************************************************************/
// Exportamos el reducer //
/*******************************************************************************************************/
export default cuestionarioReducer;
