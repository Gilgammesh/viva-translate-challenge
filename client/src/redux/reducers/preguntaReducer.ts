/*******************************************************************************************************/
// Importamos las dependencias //
/*******************************************************************************************************/
import types from 'configs/types';

/*******************************************************************************************************/
// Interface de Action //
/*******************************************************************************************************/
interface IAction {
	type: string;
	payload: any;
}

/*******************************************************************************************************/
// Interface del Reducer //
/*******************************************************************************************************/
export interface IPreguntaReducer {
	_id: string;
	nombre: string;
	opciones: [
		{
			opcion: string;
			isValid: boolean;
		}
	];
}

/*******************************************************************************************************/
// Estado inicial del Reducer //
/*******************************************************************************************************/
const initialState: IPreguntaReducer | null = null;

/*******************************************************************************************************/
// Definimos el reducer y sus mÃ©todos //
/*******************************************************************************************************/
const preguntaReducer = (state = initialState, { type, payload }: IAction) => {
	switch (type) {
		case types.setPregunta:
			return payload;
		case types.cleanPregunta:
			return initialState;
		default:
			return state;
	}
};

/*******************************************************************************************************/
// Exportamos el reducer //
/*******************************************************************************************************/
export default preguntaReducer;
